#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if ((n < 0) ? (n < -9) : (n > 9))
		ft_putnbr_fd(n / 10, fd);
	if (n < 0 && n > -10)
		write(fd, "-", 1);
	ft_putchar_fd((n < 0) ? ((n % 10) * (-1) + '0') : (n % 10 + '0'), fd);
}
